			style="color: var(--text); font-family: var(--font-head);"
		>
			Instagram
		</h3>
		<ul class="mb-6 space-y-2 pl-5 text-sm" style="color: var(--text-muted);">
			<li class="list-disc leading-relaxed">
				Don't switch from personal to creator/business account within the first two weeks. Wait until
				week 3.
			</li>
			<li class="list-disc leading-relaxed">
				Reels get stronger algorithmic distribution than static posts for accounts re-establishing
				their presence. Start with 2–3 Reels in weeks 2–3.
			</li>
			<li class="list-disc leading-relaxed">
				Instagram's follow/unfollow detection is aggressive. Stay under 50 follows per day and never
				do mass unfollows within a week of the initial credential change.
