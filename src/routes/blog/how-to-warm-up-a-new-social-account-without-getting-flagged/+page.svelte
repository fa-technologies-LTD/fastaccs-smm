			>
				Week 3 — Build Rhythm, Introduce Your Content
			</h3>
			<p class="mb-3 text-sm leading-relaxed" style="color: var(--text-muted);">
				By now the account has been interacting consistently for two weeks. You can step up.
			</p>
			<ul class="space-y-2 pl-5 text-sm" style="color: var(--text-muted);">
				<li class="list-disc">Post 1–2 times daily, including your actual brand or promotional content.</li>
