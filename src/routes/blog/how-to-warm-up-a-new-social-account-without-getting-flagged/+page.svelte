		<!-- Section 1: Why Platforms Watch -->
		<h2
			class="mb-4 mt-10 text-2xl font-bold"
			style="color: var(--text); font-family: var(--font-head);"
		>
			Why Platforms Watch New and Recently Changed Accounts
		</h2>
		<p class="mb-4 leading-relaxed" style="color: var(--text-muted);">
			Every platform runs some form of automated risk scoring. The triggers include:
		</p>
		<ul class="mb-6 space-y-3 pl-5" style="color: var(--text-muted);">
			<li class="list-disc leading-relaxed">
				<strong style="color: var(--text);">Credential changes (password, email, 2FA)</strong> — these
				signal a potential account takeover. Platforms flag accounts that make multiple security changes
				in rapid succession.
			</li>
			<li class="list-disc leading-relaxed">
				<strong style="color: var(--text);">Login from a new location or device</strong> — especially
				if it's a different country or city from the account's usual login history.
			</li>
			<li class="list-disc leading-relaxed">
				<strong style="color: var(--text);">Sudden spike in activity</strong> — an account that was
				quiet for weeks suddenly posting 20 times a day looks like a compromised account or bot.
			</li>
			<li class="list-disc leading-relaxed">
				<strong style="color: var(--text);">Mass follows or unfollows</strong> — one of the most
				reliable spam signals across all platforms.
			</li>
		</ul>
		<p class="mb-6 leading-relaxed" style="color: var(--text-muted);">
			The warm-up protocol is designed to make the account's behaviour look like a normal human
			user who's re-engaging with the platform — which is exactly what it is. The goal isn't to
			hide anything; it's to avoid sending signals that trigger automated reviews designed to catch
			actual bad actors.
		</p>

