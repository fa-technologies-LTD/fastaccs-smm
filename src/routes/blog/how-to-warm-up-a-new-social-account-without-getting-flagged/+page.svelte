				Week 2 — Light Engagement, First Posts
			</h3>
			<p class="mb-3 text-sm leading-relaxed" style="color: var(--text-muted);">
				Start introducing light activity. Keep it natural — the kind of behaviour a regular user
				would have.
			</p>
			<ul class="space-y-2 pl-5 text-sm" style="color: var(--text-muted);">
				<li class="list-disc">Like and comment on 5–10 posts per day in your niche.</li>
				<li class="list-disc">
					Start posting: 1 post every 1–2 days. Keep it simple — no promotional content yet.
				</li>
				<li class="list-disc">Follow 5–10 accounts per day that are genuinely relevant.</li>
				<li class="list-disc">Don't unfollow anyone yet.</li>
				<li class="list-disc">
					Reply to comments on your posts if you get any. Authentic two-way engagement is a positive
					signal.
				</li>
			</ul>
		</div>

