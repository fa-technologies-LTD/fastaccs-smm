				<li class="list-disc">Day 3: Transfer 2FA. Revoke unrecognised third-party app access.</li>
				<li class="list-disc">
					Days 4–7: Log in daily for a few minutes. Browse the feed. Like 3–5 posts. Nothing more.
				</li>
			</ul>
