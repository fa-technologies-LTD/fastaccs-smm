			<li class="list-disc leading-relaxed">
				Replies and quote posts (retweets with comment) distribute better than regular posts for
				newly active accounts. Engage with popular discussions in your niche during week 2.
			</li>
			<li class="list-disc leading-relaxed">
				Don't add X Premium in the first 7 days — subscribe once you've had a week of normal login
				activity from your location.
			</li>
			<li class="list-disc leading-relaxed">
				DM limits are tight in the first month. Don't push cold DM campaigns until week 4.
			</li>
