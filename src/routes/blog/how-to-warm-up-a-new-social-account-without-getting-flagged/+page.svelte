		</h3>
		<ul class="mb-6 space-y-2 pl-5 text-sm" style="color: var(--text-muted);">
			<li class="list-disc leading-relaxed">
				TikTok evaluates your first 3 videos very closely — they determine the initial audience
				segment the algorithm tests you with. Post high-quality content in your niche from day one of
				posting (week 2).
			</li>
			<li class="list-disc leading-relaxed">
				Consistent posting schedule matters more on TikTok than on most platforms. Post at the same
				times each day, 1–2 videos daily from week 2 onward.
			</li>
			<li class="list-disc leading-relaxed">
				Don't use banned or copyright audio in early videos — flag risk is higher for newer accounts.
