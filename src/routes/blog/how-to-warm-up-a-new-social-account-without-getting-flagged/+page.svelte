				Week 1 — Security First, No Activity
			</h3>
			<p class="mb-3 text-sm leading-relaxed" style="color: var(--text-muted);">
				This week is entirely about securing the account. Do not post. Do not follow anyone. Do not
				send messages.
			</p>
			<ul class="space-y-2 pl-5 text-sm" style="color: var(--text-muted);">
				<li class="list-disc">Day 1: Change the password. Log in from one device only.</li>
