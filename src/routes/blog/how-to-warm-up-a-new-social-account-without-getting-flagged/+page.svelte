			The first 2–4 weeks after acquiring a social media account are the most critical. Every major
			platform uses behavioural scoring during this period — and a rushed warm-up is the fastest
			way to end up with a restricted or flagged account. This guide gives you a realistic,
			week-by-week schedule across Instagram, X, TikTok, and Facebook, along with the exact
