				<li class="list-disc">
					Follow up to 20 accounts per day (stay under platform limits — X caps around 400/day but
					anything above 50 looks spammy for a warming account).
				</li>
				<li class="list-disc">
					Use Stories (Instagram) or short video (TikTok, Facebook) — these formats tend to get
					higher algorithm distribution for newer activity.
				</li>
				<li class="list-disc">Begin testing hashtags or topics — 3–5 per post, not 30.</li>
