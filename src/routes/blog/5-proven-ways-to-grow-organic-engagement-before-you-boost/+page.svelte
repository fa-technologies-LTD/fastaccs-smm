		1. Post when your specific audience is active — not when generic "best time" charts say to
	</h2>
	<p class="mb-4 leading-relaxed" style="color: var(--text-muted);">
		Generic "best time to post" charts average across millions of accounts in different time
		zones, industries, and audiences — they're a starting point, not a rule. Every platform's
		native analytics (Instagram Insights, TikTok Analytics, YouTube Studio) shows you when your own
		followers are actually online. Because engagement velocity in the first hour is one of the
		strongest ranking inputs (see our breakdown of how Instagram's algorithm works), posting into a
		window when your real audience is awake and scrolling directly improves the metric that matters
		most.
	</p>

	<h2 class="mb-4 mt-10 text-2xl font-bold" style="color: var(--text); font-family: var(--font-head);">
		2. Design for saves and shares, not just likes
	</h2>
	<p class="mb-4 leading-relaxed" style="color: var(--text-muted);">
		Instagram has explicitly stated that saves and shares carry more ranking weight than likes,
		because they reflect a viewer choosing to revisit or pass on content rather than passively
		reacting to it.<sup>1</sup> Practical formats that naturally earn saves: list-based or
		"reference" posts people want to come back to (recipes, checklists, step-by-step guides),
		and carousel posts that reward a second look. Formats that naturally earn shares: content with
		a strong opinion, a relatable moment, or something genuinely useful to forward to a specific
		friend.
	</p>

	<h2 class="mb-4 mt-10 text-2xl font-bold" style="color: var(--text); font-family: var(--font-head);">
		3. Reply to every comment in the first hour
	</h2>
	<p class="mb-4 leading-relaxed" style="color: var(--text-muted);">
		Comment threads — not just comment counts — are a stronger signal than isolated comments,
		because back-and-forth exchanges indicate sustained interest. Replying quickly also triggers a
		second notification to the commenter, which often pulls them back to re-engage, extending the
		thread further during the exact window when velocity matters most.
	</p>

	<h2 class="mb-4 mt-10 text-2xl font-bold" style="color: var(--text); font-family: var(--font-head);">
		4. Use platform-native formats instead of repurposed content
	</h2>
	<p class="mb-4 leading-relaxed" style="color: var(--text-muted);">
		Content built specifically for a platform's native format — vertical video without a visible
		watermark from another app, captions sized for the actual aspect ratio, hooks tailored to that
		platform's scroll speed — consistently outperforms cross-posted content. TikTok and Instagram
		have both signaled (through creator-facing guidance and product changes) that they deprioritize
		content carrying watermarks from competing platforms. A few minutes of re-formatting often
		outperforms hours of new content creation.
	</p>

	<h2 class="mb-4 mt-10 text-2xl font-bold" style="color: var(--text); font-family: var(--font-head);">
		5. Build a consistent posting rhythm — frequency matters less than predictability
	</h2>
	<p class="mb-4 leading-relaxed" style="color: var(--text-muted);">
		The algorithmic benefit of consistency isn't mystical — it's about giving the ranking system a
		stable, recent behavior pattern to model your account against. An account that posts three
		times a week, every week, gives cleaner signal than one that posts ten times one week and zero
		the next three. If you can only commit to a sustainable cadence, pick one and stick to it rather
		than front-loading and burning out.
	</p>

